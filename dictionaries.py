#Dictionaries are used to store data values in key:value pairs.
#A dictionary is a collection which is ordered*, changeable and do not allow duplicates.
# A Dictionary is a type of Object

#Creating a Dictionary

car = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
